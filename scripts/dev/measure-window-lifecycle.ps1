  $deadline = (Get-Date).AddSeconds($StartupTimeoutSeconds)
  do {
    try {
      $targets = Invoke-RestMethod -Uri "http://127.0.0.1:$RemoteDebugPort/json" -TimeoutSec 2
      if ($targets.Count -gt 0) {
        break
      }
    } catch {
      Start-Sleep -Milliseconds 500
    }
  } while ((Get-Date) -lt $deadline)

  if (-not $targets -or $targets.Count -eq 0) {
    throw "WebView2 CDP target did not appear on port $RemoteDebugPort"
  }

  $events = @()
  $snapshots = @()

  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "refresh-actions" $runDir
  $snapshots += Get-CopicuMemorySnapshot "idle-hidden" $runDir
  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "open-settings" $runDir
  Start-Sleep -Seconds 2
  $snapshots += Get-CopicuMemorySnapshot "settings-first-open" $runDir
  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "close-settings" $runDir
  Start-Sleep -Seconds 2
  $snapshots += Get-CopicuMemorySnapshot "settings-hidden-after-close" $runDir
  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "open-settings" $runDir
  Start-Sleep -Seconds 2
  $snapshots += Get-CopicuMemorySnapshot "settings-reopen-cached" $runDir
  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "open-ai-output" $runDir
  Start-Sleep -Seconds 2
  $snapshots += Get-CopicuMemorySnapshot "ai-output-first-open" $runDir
  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "close-ai-output" $runDir
  Start-Sleep -Seconds 2
  $snapshots += Get-CopicuMemorySnapshot "ai-output-after-close" $runDir
  $aiReopen = Invoke-CdpLifecycleStep $RemoteDebugPort "open-ai-output" $runDir -AllowFailure
  $events += $aiReopen
  if ($aiReopen.status -ne "failed") {
    Start-Sleep -Seconds 2
    $snapshots += Get-CopicuMemorySnapshot "ai-output-reopen" $runDir
  }
  Set-SyntheticScriptsSetting $AppDataDir $scriptDirs.Ui
  $events += Invoke-CdpLifecycleStep $RemoteDebugPort "refresh-actions" $runDir
  $uiHostOpen = Invoke-CdpLifecycleStep $RemoteDebugPort "open-ui-host" $runDir -AllowFailure
  $events += $uiHostOpen
  if ($uiHostOpen.status -ne "failed") {
