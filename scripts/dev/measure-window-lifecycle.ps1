function Set-SyntheticScriptsSetting($AppDataDir, $ScriptsDir) {
  $dbPath = Join-Path $AppDataDir "copicu.sqlite3"
  if (-not (Test-Path -LiteralPath $dbPath)) {
    return
  }

  $settings = @{
    schemaVersion = 1
    general = @{
      globalShortcut = "Ctrl+Shift+,"
      launchOnStartup = $false
    }
    picker = @{
      hideOnFocusLost = $true
      enterAction = "copy"
    }
    history = @{
      retentionCount = 0
    }
    appearance = @{
      theme = "system"
      themeId = "default"
    }
    scripts = @{
      folderPath = $ScriptsDir
    }
    ai = @{
      enabled = $false
      endpoint = "https://openrouter.ai/api/v1"
      model = "openai/gpt-4.1-mini"
    }
  }
  $settingsJson = ($settings | ConvertTo-Json -Depth 8 -Compress).Replace("'", "''")
  $now = [DateTimeOffset]::UtcNow.ToUnixTimeMilliseconds()
  $sql = "INSERT INTO app_settings (key, value_json, updated_at_unix_ms) VALUES ('app', '$settingsJson', $now) ON CONFLICT(key) DO UPDATE SET value_json = excluded.value_json, updated_at_unix_ms = excluded.updated_at_unix_ms;"
  $sql | sqlite3 $dbPath
  if ($LASTEXITCODE -ne 0) {
    throw "failed to prepare synthetic scripts setting in $dbPath"
  }
}

function Invoke-CdpLifecycleStep($Port, $Step, $RunDir, [switch] $AllowFailure) {
  $nodeScript = @'
const { chromium } = require("@playwright/test");

const port = Number(process.argv[2]);
const step = process.argv[3];
const scriptsDir = process.argv[4];

function now() {
  return Date.now();
}

async function findPage(context, predicate, timeoutMs = 10000) {
  const deadline = now() + timeoutMs;
  while (now() < deadline) {
    for (const page of context.pages()) {
      const ok = await predicate(page).catch(() => false);
      if (ok) return page;
    }
    await new Promise((resolve) => setTimeout(resolve, 100));
  }
  return null;
}

async function isSettingsPage(page) {
  return page.evaluate(() => document.body.classList.contains("settings-window"));
}

async function isAiOutputPage(page) {
  return page.evaluate(() => document.body.classList.contains("ai-output-window"));
}

async function isUiHostPage(page) {
  return page.evaluate(() => document.body.classList.contains("ui-host-window"));
}

async function runAction(mainPage, actionId) {
  return mainPage.evaluate((id) => window.__TAURI_INTERNALS__.invoke("run_action", {
    request: {
      actionId: id,
      context: {
        trigger: "devRun",
        shortcut: null,
        currentItemId: null,
        selectedItemIds: [],
        view: null,
      },
    },
  }), actionId);
}

function writeSyntheticClipboard(marker) {
  const { execFileSync } = require("child_process");
  execFileSync("cmd.exe", ["/c", "clip"], { input: marker, stdio: ["pipe", "ignore", "ignore"] });
}

async function main() {
  const browser = await chromium.connectOverCDP(`http://127.0.0.1:${port}`);
  const context = browser.contexts()[0];
  const mainPage = await findPage(
    context,
    (page) => page.evaluate(() =>
      Boolean(window.__TAURI_INTERNALS__) &&
      !document.body.classList.contains("settings-window") &&
      !document.body.classList.contains("ai-output-window") &&
      !document.body.classList.contains("ui-host-window")
    ),
    30000,
  );
  if (!mainPage) throw new Error("main page not found");

  const startedAt = now();
  let settingsPage = null;
  let aiOutputPage = null;
  let uiHostPage = null;
  let result = {};

  if (step === "refresh-actions") {
    const refreshResult = await mainPage.evaluate(() => window.__TAURI_INTERNALS__.invoke("refresh_script_action_cache"));
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      scriptCount: Array.isArray(refreshResult) ? refreshResult.length : null,
    };
  } else if (step === "open-settings") {
    await mainPage.evaluate(() => window.__TAURI_INTERNALS__.invoke("open_settings_window"));
    settingsPage = await findPage(context, isSettingsPage, 15000);
    if (!settingsPage) throw new Error("settings page not found after open");
    await settingsPage.waitForSelector(".settings-window-app", { timeout: 15000 });
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      settingsBodyClass: await settingsPage.evaluate(() => document.body.className),
      settingsTextSample: await settingsPage.evaluate(() => document.body.innerText.slice(0, 80)),
    };
  } else if (step === "close-settings") {
    settingsPage = await findPage(context, isSettingsPage, 5000);
    if (!settingsPage) throw new Error("settings page not found before close");
    await settingsPage.evaluate(() => window.__TAURI_INTERNALS__.invoke("close_settings_window"));
    await new Promise((resolve) => setTimeout(resolve, 1000));
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      settingsPageStillInCdp: Boolean(await findPage(context, isSettingsPage, 500)),
    };
  } else if (step === "open-ai-output") {
    writeSyntheticClipboard(`COPICU_SYNTH_PERF_AI_OUTPUT ${Date.now()}`);
    aiOutputPage = await findPage(context, isAiOutputPage, 30000);
    if (!aiOutputPage) throw new Error("ai-output page not found after action");
    await aiOutputPage.waitForSelector(".ai-output-document", { timeout: 15000 });
    writeSyntheticClipboard(`COPICU_SYNTH_PERF_AI_OUTPUT ${Date.now()} READY`);
    await aiOutputPage.waitForFunction(
      () => document.body.innerText.includes("COPICU_SYNTH_PERF AI Output"),
      { timeout: 15000 },
    );
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      trigger: "clipboardChange",
      aiOutputBodyClass: await aiOutputPage.evaluate(() => document.body.className),
      aiOutputTextSample: await aiOutputPage.evaluate(() => document.body.innerText.slice(0, 120)),
    };
  } else if (step === "close-ai-output") {
    aiOutputPage = await findPage(context, isAiOutputPage, 5000);
    if (!aiOutputPage) throw new Error("ai-output page not found before close");
    await aiOutputPage.evaluate(() => window.close());
    await new Promise((resolve) => setTimeout(resolve, 1000));
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      aiOutputPageStillInCdp: Boolean(await findPage(context, isAiOutputPage, 500)),
    };
  } else if (step === "open-ui-host") {
    writeSyntheticClipboard(`COPICU_SYNTH_PERF_UI_HOST ${Date.now()}`);
    uiHostPage = await findPage(context, isUiHostPage, 30000);
    if (!uiHostPage) throw new Error("ui-host page not found after action");
    writeSyntheticClipboard(`COPICU_SYNTH_PERF_UI_HOST ${Date.now()} READY`);
    await uiHostPage.waitForSelector(".ui-host-panel", { timeout: 15000 });
    await uiHostPage.waitForFunction(
      () => document.body.innerText.includes("COPICU_SYNTH_PERF UI Host"),
      { timeout: 15000 },
    );
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      trigger: "clipboardChange",
      uiHostBodyClass: await uiHostPage.evaluate(() => document.body.className),
      uiHostTextSample: await uiHostPage.evaluate(() => document.body.innerText.slice(0, 120)),
    };
  } else if (step === "close-ui-host") {
    uiHostPage = await findPage(context, isUiHostPage, 5000);
    if (!uiHostPage) throw new Error("ui-host page not found before close");
    await uiHostPage.getByRole("button", { name: "Measure" }).click();
    await new Promise((resolve) => setTimeout(resolve, 1000));
    result = {
      step,
      elapsedMs: now() - startedAt,
      pageCount: context.pages().length,
      uiHostPageStillInCdp: Boolean(await findPage(context, isUiHostPage, 500)),
    };
  } else {
    throw new Error(`unknown step: ${step}`);
  }

  await browser.close();
  console.log(JSON.stringify(result));
}

main().catch((error) => {
  console.error(error.stack || String(error));
  process.exit(1);
});
