    appDataDir = $AppDataDir
    scriptsDir = $scriptDirs.Root
    ownerPid = $owner.Id
    events = $events
    memory = $snapshots
  } | ConvertTo-Json -Depth 8 | Tee-Object -FilePath (Join-Path $runDir "summary.json")
