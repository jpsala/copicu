  -ArgumentList @("tauri", "dev", "--no-watch", "--config", "src-tauri/tauri.built-dev.conf.json") `
  -WorkingDirectory $repoRoot `
  -RedirectStandardOutput $stdoutLog `
  -RedirectStandardError $stderrLog `
  -WindowStyle Hidden `
  -PassThru

